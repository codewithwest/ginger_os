#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
# LFS=/mnt/lfs;
sleep 5;
# Extract package 
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep grep | grep tar);
echo "################################################################################";
echo "#---------------------------Extracting e2fsprogs package------------------------#";
echo "################################################################################";
echo;echo;echo;
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/e2fsprogs" | grep ".tar");
echo "################################################################################";
echo "#--------------------Extracting binutils archive complete----------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------------Change into e2fsprogs dir--------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/e2fsprogs");
pwd;
sleep 2;
# The E2fsprogs documentation recommends that the package be built in a subdirectory of the source tree:
echo "################################################################################";
echo "#-------------------------------Create build dir-------------------------------#";
echo "################################################################################";
echo;echo;echo;
mkdir -v build
sleep 2;
echo "################################################################################";
echo "#--------------------------Create build dir complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------------Change into build dir-----------------------------#";
echo "################################################################################";
echo;echo;echo;
mkdir -v build
sleep 2;
echo "################################################################################";
echo "#----------------------Change into build dir complete--------------------------#";
echo "################################################################################";
echo;echo;echo;
cd build
sleep 1;
pwd;
sleep 2;
echo "################################################################################";
echo "#------------------------------Configure tool----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
../configure --prefix=/usr           \
             --sysconfdir=/etc       \
             --enable-elf-shlibs     \
             --disable-libblkid      \
             --disable-libuuid       \
             --disable-uuidd         \
             --disable-fsck;
echo "################################################################################";
echo "#-------------------------Configure tool completed-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------------------------------Make tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
time make;
echo "################################################################################";
echo "#---------------------------Make tool complete---------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Test suite-----------------------------------#";
echo "################################################################################";
echo;echo;echo;
make check;
echo "################################################################################";
echo "#--------------------------------Test suite complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------------------------Install tool------------------------------------#";
echo "################################################################################";
echo;echo;echo;
make install;
echo "################################################################################";
echo "#------------------------------Install tool complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# Remove useless static libraries:
echo "################################################################################";
echo "#--------------------------Remove useless static libraries---------------------#";
echo "################################################################################";
echo;echo;echo;
rm -fv /usr/lib/{libcom_err,libe2p,libext2fs,libss}.a;
echo "################################################################################";
echo "#----------------Remove useless static libraries complete----------------------#";
echo "################################################################################";    
echo;echo;echo;
sleep 2;
# This package installs a gzipped .info file but doesn't update the system-wide dir file. 
# Unzip this file and then update the system dir file using the following commands:
echo "################################################################################";
echo "#-----------------Update the system-wide dir file------------------------------#";
echo "################################################################################";
echo;echo;echo; 
gunzip -v /usr/share/info/libext2fs.info.gz;
sleep 1;
install-info --dir-file=/usr/share/info/dir /usr/share/info/libext2fs.info;
echo "################################################################################";
echo "#------------------Update the system-wide dir file complete--------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# If desired, create and install some additional documentation by issuing the following commands:
echo "################################################################################";
echo "#-----------------Create and install additional documentation------------------#";
echo "################################################################################";
echo;echo;echo;
akeinfo -o      doc/com_err.info ../lib/et/com_err.texinfo;
sleep 1;
install -v -m644 doc/com_err.info /usr/share/info;
sleep 1;
install-info --dir-file=/usr/share/info/dir /usr/share/info/com_err.info;
echo "################################################################################";
echo "#----------------Create and install additional documentation complete----------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# /etc/mke2fs.conf contains the default value of various command line options of mke2fs. 
# You may edit the file to make the default values suitable for your need. For example, 
# some utilities (not in LFS or BLFS) cannot recognize a ext4 file system with 
# metadata_csum_seed feature enabled. If you need such an utility, you may remove the 
# feature from the default ext4 feature list with the command:
echo "################################################################################";
echo "#-------------------Edit the /etc/mke2fs.conf file-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sed 's/metadata_csum_seed,//' -i /etc/mke2fs.conf;
echo "################################################################################";
echo "#-------------------Edit the /etc/mke2fs.conf file complete--------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------------------Clean Up -----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $LFS/sources;
sleep 2;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/e2fsprogs");
echo "################################################################################";
echo "#--------------------------Clean Up Complete-----------------------------------#";
echo "################################################################################";
echo;echo;echo;