#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
# LFS=/mnt/lfs;
sleep 5;
# Extract package 
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep Readline | grep tar);
echo "################################################################################";
echo "#----------------------------Extracting Readline package-----------------------#";
echo "################################################################################";
echo;echo;echo;
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/Readline" | grep ".tar");
echo "################################################################################";
echo "#---------------------Extracting Readline archive complete---------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# change into the extracted package
echo "################################################################################";
echo "#--------------------------Change into Readline dir----------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/Readline");
pwd;
sleep 2;
echo "################################################################################";
echo "#----------------------------Apply Readline patch------------------------------#";
echo "################################################################################";
echo;echo;echo;
patch -Np1 -i $(find "$LFS/sources" -type f | grep "$LFS/sources/Readline" | grep "patch");
sleep 2;
echo "################################################################################";
echo "#------------------------Apply Readline patch complete-------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------ensure installation of symbolic links are relative----------------#";
echo "################################################################################";
echo;echo;echo;
sed -i 's@\(ln -s -f \)$(PREFIX)/bin/@\1@' Makefile;
sleep 2;
echo "################################################################################";
echo "#-------ensure installation of symbolic links are relative complete------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------Ensure the man pages are installed into the correct location--------#";
echo "################################################################################";
echo;echo;echo;
sed -i "s@(PREFIX)/man@(PREFIX)/share/man@g" Makefile;
sleep 2;
echo "################################################################################";
echo "#-----------man pages are installed into the correct location complete---------#";
echo "################################################################################";
sleep 2;
echo "################################################################################";
echo "#---------------------Prepare Readline for compilation-------------------------#";
echo "################################################################################";
echo;echo;echo;
make -f Makefile-libbz2_so;
make clean;
sleep 2;
echo "################################################################################";
echo "#---------------------Prepare Readline for compilation complete----------------#";
echo "################################################################################";
sleep 2;
echo "################################################################################";
echo "#------------------------------------Make tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
time make;
echo "################################################################################";
echo "#---------------------------Make tool complete---------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Install tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
make PREFIX=/usr install;
echo "################################################################################";
echo "#------------------------------Install tool complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#-----------------------Install the shared library-----------------------------#";
echo "################################################################################";
echo;echo;echo;
cp -av libbz2.so.* /usr/lib;
ln -sv libbz2.so.1.0.8 /usr/lib/libbz2.so;
echo "################################################################################";
echo "#------------------Install the shared library complete-------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#-----Install the shared Readline binary into the /usr/bin directory-----------#";
echo "#------------and replace two copies of Readline with symlinks:-----------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cp -v Readline-shared /usr/bin/Readline;
sleep 1;
for i in /usr/bin/{bzcat,bunzip2}; do
  ln -sfv Readline $i
done
echo "################################################################################";
echo "#-------Install the shared Readline binary into the /usr/bin directory---------#";
echo "#----------and replace two copies of Readline with symlinks complete-----------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------Remove a useless static library:------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
rm -fv /usr/lib/libbz2.a;
echo "################################################################################";
echo "#------------------Remove a useless static library complete--------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------------------Clean Up -----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $LFS/sources;
sleep 2;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/Readline");
echo "################################################################################";
echo "#--------------------------Clean Up Complete-----------------------------------#";
echo "################################################################################";
echo;echo;echo;