#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
# LFS=/mnt/lfs;
sleep 5;
# Extract package 
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep readline | grep tar);
echo "################################################################################";
echo "#----------------------------Extracting readline package-----------------------#";
echo "################################################################################";
echo;echo;echo;
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/readline" | grep ".tar");
echo "################################################################################";
echo "#---------------------Extracting readline archive complete---------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# change into the extracted package
echo "################################################################################";
echo "#--------------------------Change into readline dir----------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/readline");
pwd;
sleep 2;
# Reinstalling readline will cause the old libraries to be moved to <libraryname>.old. 
# While this is normally not a problem, in some cases it can trigger a linking bug in 
# ldconfig. This can be avoided by issuing the following two seds:
echo "################################################################################";
echo "#-------------------Avoid linking bug in ldconfig.-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sed -i '/MV.*old/d' Makefile.in
sed -i '/{OLDSUFF}/c:' support/shlib-install
sleep 2;
echo "################################################################################";
echo "#-------------------Avoid linking bug in ldconfig complete---------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#--Prevent hard coding library search paths (rpath) into the shared libraries--#";
echo "################################################################################";
echo;echo;echo;
sed -i 's/-Wl,-rpath,[^ ]*//' support/shobj-conf
sleep 2;
echo "################################################################################";
echo "#---------------------Prepare readline for compilation-------------------------#";
echo "################################################################################";
echo;echo;echo;
./configure --prefix=/usr    \
            --disable-static \
            --with-curses    \
            --docdir=/usr/share/doc/readline-8.2.13;
sleep 2;
echo "################################################################################";
echo "#---------------------Prepare readline for compilation complete----------------#";
echo "################################################################################";
sleep 2;
echo "################################################################################";
echo "#------------------------------------Make tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
time make SHLIB_LIBS="-lncursesw";
echo "################################################################################";
echo "#---------------------------Make tool complete---------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Install tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
make SHLIB_LIBS="-lncursesw" install;
echo "################################################################################";
echo "#------------------------------Install tool complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------- install the documentation-----------------------------#";
echo "################################################################################";
echo;echo;echo;
install -v -m644 doc/*.{ps,pdf,html,dvi} /usr/share/doc/readline-8.2.13;
echo "################################################################################";
echo "#-------------------install the documentation complete-------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#----------------------------------Clean Up -----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $LFS/sources;
sleep 2;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/readline");
echo "################################################################################";
echo "#--------------------------Clean Up Complete-----------------------------------#";
echo "################################################################################";
echo;echo;echo;