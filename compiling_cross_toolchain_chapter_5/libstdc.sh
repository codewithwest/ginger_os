#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
LFS=/mnt/lfs;
sleep 5;
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep libstdc | grep tar )
echo "################################################################################";
echo "#--------------------------Extract libstdc package-----------------------------#";
echo "################################################################################";
echo;echo;echo;
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/gcc" | grep ".tar")
echo "################################################################################";
echo "#-------------------Extracting libstdc package complete------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 5;
echo "################################################################################";
echo "#----------------------------change into libstdc dir---------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/gcc")
pwd;
echo "################################################################################";
echo "#--------------------------create a build dir----------------------------------#";
echo "################################################################################";
echo;echo;echo;
mkdir -v build;
sleep 5;
echo "################################################################################";
echo "#-------------------------change into build dir--------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd build;
sleep 5;
echo "################################################################################";
echo "#-----------------------------configure package--------------------------------#";
echo "################################################################################";
echo;echo;echo;
../libstdc++-v3/configure           \
    --host=$LFS_TGT                 \
    --build=$(../config.guess)      \
    --prefix=/usr                   \
    --disable-multilib              \
    --disable-nls                   \
    --disable-libstdcxx-pch         \
    --with-gxx-include-dir=/tools/$LFS_TGT/include/c++/13.2.0;
echo "################################################################################";
echo "#--------------------------configure package complete--------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 5;
time make;
sleep 5;
echo "################################################################################";
echo "#---------------------------------make package complete------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 5;
echo "################################################################################";
echo "#---------------------------------install package------------------------------#";
echo "################################################################################";
echo;echo;echo;
make DESTDIR=$LFS install;
echo "################################################################################";
echo "#-------------------------install package complete-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 5;
# Remove the libtool archive files because they are harmful for cross-compilation:
echo "################################################################################";
echo "#-Remove the libtool archive files because they are harmful for cross-compile--#";
echo "################################################################################";
echo;echo;echo;
rm -v $LFS/usr/lib/lib{stdc++{,exp,fs},supc++}.la;
sleep 2;
echo "################################################################################";
echo "#------------------------------- clean up script-------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/gcc");
echo "################################################################################";
echo "#---------------------------clean up script complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 5;