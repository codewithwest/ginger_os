#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
# LFS=/mnt/lfs;
sleep 5;
# Extract package 
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep binutils | grep tar);
echo "################################################################################";
echo "#----------------------------Extracting lz4 package-----------------------------#";
echo "################################################################################";
echo;echo;echo;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep lz4 | grep tar);
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/lz4" | grep ".tar");
echo "################################################################################";
echo "#--------------------Extracting lz4 archive complete----------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# change into the extracted package
echo "################################################################################";
echo "#-----------------------------Change into lz4 dir-------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/lz4");
pwd;
sleep 2;
echo "################################################################################";
echo "#-------------------------Compile the package----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
make BUILD_STATIC=no PREFIX=/usr;
echo "################################################################################";
echo "#--------------------Compile the package completed-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------------------To test the results, issue:---------------------------#";
echo "################################################################################";
echo;echo;echo;
make -j1 check;
echo "################################################################################";
echo "#-------------------To test the results, issue complete------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Install tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
make BUILD_STATIC=no PREFIX=/usr install;
echo "################################################################################";
echo "#------------------------------Install tool complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
echo "################################################################################";
echo "#----------------------------------Clean Up -----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $LFS/sources;
sleep 2;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/lz4");
echo "################################################################################";
echo "#--------------------------Clean Up Complete-----------------------------------#";
echo "################################################################################";
echo;echo;echo;