#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
LFS=/mnt/lfs;
sleep 5;
# Extract package 
echo "################################################################################";
echo "#-------------------------Change into sources dir------------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $LFS/sources;
pwd;
sleep 5;
# tar -xvf $LFS/sources/$(cd $LFS/sources/ &&  cat wget-list | grep coreutils | grep tar );
echo "################################################################################";
echo "#-------------------------Extracting coreutils package-------------------------#";
echo "################################################################################";
echo;echo;echo;
tar -xvf $(find "$LFS/sources" -type f | grep "$LFS/sources/coreutils" | grep ".tar");
echo "################################################################################";
echo "#-------------------Extracting coreutils archive complete----------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#--------------------------Change into binutils dir----------------------------#";
echo "################################################################################";
echo;echo;echo;
cd $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/coreutils");
pwd;
sleep 2;
echo "################################################################################";
echo "#-------------------------------Patch coreutils--------------------------------#";
echo "################################################################################";
echo;echo;echo;
patch -Np1 -i ../coreutils-9.5-i18n-2.patch;
sleep 2;
echo "################################################################################";
echo "#---------------------------Patch coreutils complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------------------------Configure tool----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
autoreconf -fiv;
sleep 2;
FORCE_UNSAFE_CONFIGURE=1 ./configure \
            --prefix=/usr            \
            --enable-no-install-program=kill,uptime;
echo "################################################################################";
echo "#-------------------------Configure tool completed-----------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#------------------------------------Make tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
time make;
echo "################################################################################";
echo "#---------------------------Make tool complete---------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Test suite-----------------------------------#";
echo "################################################################################";
echo;echo;echo;
make NON_ROOT_USERNAME=tester check-root;
sleep 2;
groupadd -g 102 dummy -U tester;
sleep 2;
chown -R tester .;
sleep 2;
su tester -c "PATH=$PATH make -k RUN_EXPENSIVE_TESTS=yes check" \
   < /dev/null
sleep 2;
groupdel dummy;
echo "################################################################################";
echo "#--------------------------------Test suite complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
echo "################################################################################";
echo "#---------------------------------Install tool---------------------------------#";
echo "################################################################################";
echo;echo;echo;
make install;
sleep 2;
mv -v /usr/bin/chroot /usr/sbin;
sleep 2;
mv -v /usr/share/man/man1/chroot.1 /usr/share/man/man8/chroot.8;
sleep 2;
sed -i 's/"1"/"8"/' /usr/share/man/man8/chroot.8
echo "################################################################################";
echo "#------------------------------Install tool complete---------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
# Move programs to their final expected locations. Although this is not necessary in 
# this temporary environment, we must do so because some programs hardcode executable locations:
echo "################################################################################";
echo "#--------------Move programs to their final expected locations.----------------#"; 
echo "#------we must do so because some programs hardcode executable locations-------#";
echo "################################################################################";
echo;echo;echo;
mv -v $LFS/usr/bin/chroot              $LFS/usr/sbin;
mkdir -pv $LFS/usr/share/man/man8;
mv -v $LFS/usr/share/man/man1/chroot.1 $LFS/usr/share/man/man8/chroot.8;
sed -i 's/"1"/"8"/'                    $LFS/usr/share/man/man8/chroot.8;
sleep 5;
echo "################################################################################";
echo "#----------------------------------Clean Up -----------------------------------#";
echo "################################################################################";
echo;echo;echo;
sleep 2;
cd $LFS/sources;
sleep 2;
rm -rf $(find "$LFS/sources" -type d | grep -m1 "$LFS/sources/coreutils");
echo "################################################################################";
echo "#--------------------------Clean Up Complete-----------------------------------#";
echo "################################################################################";
echo;echo;echo;