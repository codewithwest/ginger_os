#!/bin/bash
echo "################################################################################";
echo "#--------------------------Setting LFS variable--------------------------------#";
echo "################################################################################";
echo;echo;echo;
LFS=/mnt/lfs;
sleep 5;
echo "################################################################################";
echo "#------------------------------Creating LFS Dir--------------------------------#";
echo "################################################################################";
mkdir -pv $LFS;
echo "################################################################################";
echo "#--------------------------LFS Dir creation complete---------------------------#";
echo "################################################################################";
sleep 2;
# mount root to lfs
echo "################################################################################";
echo "#-------------------------Mounting Root to LFS Dir-----------------------------#";
echo "################################################################################";
mount -v -t ext4 /dev/sda4 $LFS;
echo "################################################################################";
echo "#----------------------Mounting Root to LFS Dir Complete-----------------------#";
echo "################################################################################";
sleep 2;
# mount boot to lfs
echo "################################################################################";
echo "#------------------------------Creating LFS Boot Dir---------------------------#";
echo "################################################################################";
mkdir -pv $LFS/boot;
echo "################################################################################";
echo "#------------------------Creating LFS Boot Dir Complete------------------------#";
echo "################################################################################";
sleep 2;
echo "################################################################################";
echo "#-------------------------Mounting Boot to LFS Dir-----------------------------#";
echo "################################################################################";
mount -v -t ext4 /dev/sda1 $LFS/boot;
echo "################################################################################";
echo "#----------------------Mounting Boot to LFS Dir Complete-----------------------#";
echo "################################################################################";
sleep 2;
# If you are using a swap partition, 
# ensure that it is enabled using the swapon command:
echo "################################################################################";
echo "#-------------------------Mounting Swap to LFS Dir-----------------------------#";
echo "################################################################################";
/sbin/swapon -v /dev/sda3
echo "################################################################################";
echo "#----------------------Mounting Swap to LFS Dir Complete-----------------------#";
echo "################################################################################";